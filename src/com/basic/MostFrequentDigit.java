package com.basic;

public class switchcase {
	public static void main(String[] args)
	{
	  int count;
	   switch (count = 2 )
	   {
	   case 1:
		   System.out.println("1");
		   break;
	   case 2:
		   System.out.println("2");
		   break;
		default:
			  System.out.println("none");
			  break;
	   }	 
	}	
}
